// Operasi Dasar pada Array
let laptop = ["LENOVO", "MSI", "ASUS"];
console.log(laptop[0]);
console.log(laptop[1]);

// Tambah elemen di akhir
laptop.push("ACER");
console.log(laptop);

// Hapus elemen terakhir
laptop.pop();
console.log(laptop);

// Tambah di awal
laptop.unshift("AXIOO");
console.log(laptop);

// Hapus di awal
laptop.shift();
console.log(laptop);

// Panjang array
console.log(laptop.length);

